package com.rqpa.algo.coordinates;

public class TwoDimensionalIntCoordinates
{
    private final int x;
    private final int y;

    public TwoDimensionalIntCoordinates(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
